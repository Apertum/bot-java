package ru.mail.im.botapi.fetcher;

public class User {
    private String firstName;
    private String lastName;
    private String nick;
    private String userId;

    public String getFirstName() {
        return firstName;
    }

    public String getNick() {
        return nick;
    }

    public String getUserId() {
        return userId;
    }

    public String getLastName() {
        return lastName;
    }
}